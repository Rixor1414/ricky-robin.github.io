function drawChessboard(num) {
    var i = 0;
    var logger = "";
  
    for (var i = 0; i < num; i++) {
      for (var j = 0; j < num; j++) {
        if ((i + j) % 2 === 0) {
          logger += " ";
        } else {
          logger += "#";
        }
      }
      logger += "\n";
    }
  
    console.log(logger);
  }
